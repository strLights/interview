<p>SSL（Secure Sockets Layer）安全套接层协议，目的是保证数据传输的安全和完整。SSL协议既用到了对称加密也用到了非对称加密 (公钥加密)，在建立传输链路时，SSL首先对对称加密的密钥使用公钥进行非对称加密，链路建立好之后，SSL对传输内容使用对称加密。这样，既利用了非对称加密在公开环境下安全传输密钥的优点，又利用了对称加密算法在大量数据加密时的效率优势。</p>
